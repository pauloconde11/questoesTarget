


/*

4) Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em uma sala diferente.
Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser.
Seu objetivo é descobrir qual interruptor controla qual lâmpada.
Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, qual interruptor controla cada lâmpada?




Primeira visita:

Ligue o primeiro interruptor por um tempinho e depois desligue.
Ligue o segundo interruptor e entre na sala.
O que pode acontecer:

Se a luz estiver acesa, o segundo interruptor é quem controla essa luz, o primeiro controla a luz que ficou apagada.
Se a luz estiver apagada, é o primeiro interruptor que controla essa luz, o segundo controla a luz que ficou apagada.
Segunda visita:

Ligue o terceiro interruptor.
O que pode acontecer:

Se a luz estiver acesa, então é o terceiro interruptor que a controla.
Se a luz estiver apagada, então o terceiro interruptor não tem nada a ver com essa luz.
Dessa forma, com apenas duas visitas, você consegue saber qual interruptor controla cada lâmpada.

*/

